    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        //       String criterioBusqueda = jTextField1.getText();
        ControlUsuario objCu = new ControlUsuario();
        int ncol;
        Object[] fila;
        //        if (criterioBusqueda.equals("")) {
            listaUsuario = objCu.consultarUsuarios();

            DefaultTableModel modelo = new DefaultTableModel();
            this.jTable1.setModel(modelo);

            modelo.addColumn("Cedula");
            modelo.addColumn("Usuario");
            modelo.addColumn("Fecha Nacimiento");
            modelo.addColumn("Correo");
            modelo.addColumn("Contrase√±a");
            modelo.addColumn("Telefono");
            modelo.addColumn("Foto Perfil");
            modelo.addColumn("Direccion");
            ncol = modelo.getColumnCount();

            //Object[] fila = new Object[ncol];
            for (int i = 0; i < listaUsuario.size(); i++) {
                fila = new Object[ncol];
                fila[0] = listaUsuario.get(i).getCedula();
                fila[1] = listaUsuario.get(i).getNick();
                fila[2] = listaUsuario.get(i).getFechaNacimiento();
                fila[3] = listaUsuario.get(i).getCorreoU();
                fila[4] = listaUsuario.get(i).getContrasenaU();
                fila[5] = listaUsuario.get(i).getTelefonoU();
                fila[6] = listaUsuario.get(i).getFotoPerfil();
                fila[7] = listaUsuario.get(i).getDireccionU();

                modelo.addRow(fila);
                ContarRegistros();
            }
            //        }
    }//GEN-LAST:event_jButton4ActionPerformed

